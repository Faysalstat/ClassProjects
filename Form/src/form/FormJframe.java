    private String getValuefromCheckBox() {
        String educ = " ";
        for (int i = 0; i < jPanel1.getComponentCount(); i++) {
            JCheckBox jc = (JCheckBox) jPanel1.getComponent(i);
            if (jc.isSelected()) {
                educ += jc.getText() + " ";
            }

        }
        return educ;
    }

